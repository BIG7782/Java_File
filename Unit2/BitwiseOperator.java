
package Unit2;
public class BitwiseOperator {
    public static void main (String[] args)
    {
        int a=5,b=3;
        System.out.println("a&b="+(a&b));
        System.out.println("a|b="+(a|b));
        System.out.println("b<<1="+(b<<1));//left shift
        System.out.println("a>>>1="+(a>>>1));//shift right
    }
}
