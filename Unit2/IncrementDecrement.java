
package Unit2;
public class IncrementDecrement {
    public static void main(String[] args)
    {
        int a=5,b=10;
        System.out.println("a="+(++a)+" b = "+(b++));
        System.out.println("a="+a+" b = "+b);
        System.out.println("a="+(a--)+" b = "+(++b));
        System.out.println("a="+a+" b = "+b);
        
    }
}
